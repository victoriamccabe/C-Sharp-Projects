using System;

// Math Challenge: Perform basic arithmetic operations and display the results.
int num1 = 10;
int num2 = 20;

// Addition
int sum = num1 + num2;
Console.WriteLine($"The sum of {num1} and {num2} is {sum}");

// Subtraction
int difference = num2 - num1;
Console.WriteLine($"The difference between {num2} and {num1} is {difference}");

// Multiplication
int product = num1 * num2;
Console.WriteLine($"The product of {num1} and {num2} is {product}");

// Division
int quotient = num2 / num1;
Console.WriteLine($"The quotient of {num2} divided by {num1} is {quotient}");

// Modulus
int remainder = num2 % num1;
Console.WriteLine($"The remainder of {num2} divided by {num1} is {remainder}");

Console.ReadLine(); // Keep the console window open
